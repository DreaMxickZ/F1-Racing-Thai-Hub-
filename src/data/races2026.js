// Helper: หา race จาก slug
// Helper: หา race จาก slug — return format ที่ RaceResult คาดหวัง
export const getRaceBySlug = (slug) => {
  const r = RACES_2026.find((r) => 
    r.slug.toLowerCase() === slug?.toLowerCase()
  ) ?? null;
  
  if (!r) return null;
  return {
    ...r,
    Sprint: r.isSprint ? { date: r.date } : null,
    Circuit: {
      circuitName: r.circuit,
      Location: { locality: r.locality, country: r.country },
    },
  };
};

// Helper: หา race ที่แสดงได้ (FP1 ผ่านไป 1 ชั่วโมงแล้ว)
export const getCompletedRaces = () => {
  const now = new Date();
  return RACES_2026.filter((race) => {
    const fp1Time = new Date(race.fp1.date + 'T' + race.fp1.time);
    fp1Time.setHours(fp1Time.getHours() + 1);
    return fp1Time < now;
  });
};